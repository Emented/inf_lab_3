import re
import turtle
t1 = turtle.Turtle()
t1.pencolor('black')
t1.pen(shown = False)
t1.pensize(7)
t1.speed(0)
t1.penup()
t1.setx(-200)
t1.sety(-50)
t1.pendown()
t1.forward(100)
t1.penup()
t1.sety(50)
t1.pendown()
t1.back(100)
t1.penup()
t1.setx(-50)
t1.sety(0)
t1.pendown()
t1.seth(30)
t1.forward(100)
t1.back(100)
t1.seth(-30)
t1.forward(100)
t1.penup()
t1.home()
t1.setx(120)
t1.seth(90)
t1.pendown()
t1.circle(-300, 35)
t1.penup()
t1.setx(120)
t1.sety(0)
t1.pendown()
t1.seth(-90)
t1.circle(300, 35)
turtle.mainloop()

